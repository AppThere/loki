//! The theme part (`ppt/theme/theme1.xml`).
//!
//! A complete-but-generic Office theme (color/font/format schemes). `PowerPoint`
//! requires a theme — referenced by the slide master — to open a presentation;
//! this provides a valid default. It is static: the exporter does not yet derive
//! a theme from the document, so authored theme colors are not preserved on
//! export (a tracked follow-up).

/// Returns the static `theme1.xml` document.
pub(super) fn theme_xml() -> &'static str {
    THEME_XML
}

